#iterate number from 1 to 50 using for loop
#create variable to save the sum
Total_Sum = 0

#using for loop along with range for 1 to 50 and add the numbers

for num in range(1,51):
    Total_Sum = Total_Sum + num

#print the total sum after the loop is completed.
print(f"The sum of numbers from 1to 50 is: {Total_Sum}")
